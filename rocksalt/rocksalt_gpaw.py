if len(sys.argv) == 1:
    names = sorted(lattice_constant.keys())
else:
    names = [sys.argv[1]]

c = ase.db.connect('rocksalt_gpaw.db')

#mode = 'lcao'
mode = 'fd'
mode = 'pw'

e = 0.08  # h -> gpts
e = round(100 * Rydberg, 0)

kptdensity = 8.0  # this is converged for width of 0.05 eV
width = 0.05

relativistic = False
constant_basis = False  # going to 85% compression would result in huge PW

linspace = np.concatenate((np.array([0.85, 0.90]),
                           np.linspace(0.95, 1.05, 5),
                           np.array([1.10, 1.15])))

code = 'gpaw' + '-' + mode + str(e) + '_c' + str(constant_basis)
code = code + '_k' + str(kptdensity) + '_w' + str(width)
code = code + '_r' + str(relativistic)

for name in names:
    # save all steps in one traj file in addition to the database
    # we should only used the database c.reserve, but here
    # traj file is used as another lock ...
    fd = opencew(name + '_' + code + '.traj')
    if fd is None:
        continue
    traj = Trajectory(name + '_' + code + '.traj', 'w')
    atoms = bulk(name, 'rocksalt', a=lattice_constant[name])
    cell = atoms.get_cell()
    kpts = tuple(kpts2mp(atoms, kptdensity, even=True))
    kwargs = {}
    if mode in ['fd', 'lcao']:
        if constant_basis:
            # gives more smooth EOS in fd mode
            kwargs.update({'gpts': h2gpts(e, cell)})
        else:
            kwargs.update({'h': e})
    elif mode == 'pw':
        if constant_basis:
            kwargs.update({'mode': PW(e, cell=cell)})
            kwargs.update({'gpts': h2gpts(0.10, cell)})
        else:
            kwargs.update({'mode': PW(e)})
    if mode == 'lcao':
        kwargs.update({'mode': 'lcao'})
        kwargs.update({'basis': 'dzp'})
    # loop over EOS linspace
    for n, x in enumerate(linspace):
        id = c.reserve(name=name, mode=mode, e=e,
                       kptdensity=kptdensity, width=width,
                       relativistic=relativistic,
                       constant_basis=constant_basis,
                       x=x)
        if id is None:
            continue
        # perform EOS step
        atoms.set_cell(cell * x, scale_atoms=True)
        # set calculator
        atoms.calc = GPAW(
            txt=name + '_' + code + '_' + str(n) + '.txt',
            xc='PBE',
            kpts=kpts,
            width=width,
            parallel={'band': 1},
            maxiter=777,
            idiotproof=False)
        atoms.calc.set(**kwargs)  # remaining calc keywords
        t = time.time()
        try:
            atoms.get_potential_energy()
            c.write(atoms,
                    name=name, mode=mode, e=e,
                    kptdensity=kptdensity, width=width,
                    relativistic=relativistic,
                    constant_basis=constant_basis,
                    x=x,
                    niter=atoms.calc.get_number_of_iterations(),
                    time=time.time()-t)
            traj.write(atoms)
        except RuntimeError:  # dataset missing
           pass
        del c[id]
